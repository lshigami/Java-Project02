package com.javaweb.DTO;

public class BuildingDTO {
	private String namOfBuilding;
	private String addressBuilding;
	private String numberOfBasement;
	private String nameOfManager;
	private String phoneOfManager;
	private String floorArea;
	private String freeArea;
	private String rentArea;
	private String feeBrokerage;
	public String getNamOfBuilding() {
		return namOfBuilding;
	}
	public void setNamOfBuilding(String namOfBuilding) {
		this.namOfBuilding = namOfBuilding;
	}
	public String getAddressBuilding() {
		return addressBuilding;
	}
	public void setAddressBuilding(String addressBuilding) {
		this.addressBuilding = addressBuilding;
	}
	public String getNumberOfBasement() {
		return numberOfBasement;
	}
	public void setNumberOfBasement(String numberOfBasement) {
		this.numberOfBasement = numberOfBasement;
	}
	public String getNameOfManager() {
		return nameOfManager;
	}
	public void setNameOfManager(String nameOfManager) {
		this.nameOfManager = nameOfManager;
	}
	public String getPhoneOfManager() {
		return phoneOfManager;
	}
	public void setPhoneOfManager(String phoneOfManager) {
		this.phoneOfManager = phoneOfManager;
	}
	public String getFloorArea() {
		return floorArea;
	}
	public void setFloorArea(String floorArea) {
		this.floorArea = floorArea;
	}
	public String getFreeArea() {
		return freeArea;
	}
	public void setFreeArea(String freeArea) {
		this.freeArea = freeArea;
	}
	public String getRentArea() {
		return rentArea;
	}
	public void setRentArea(String rentArea) {
		this.rentArea = rentArea;
	}
	public String getFeeBrokerage() {
		return feeBrokerage;
	}
	public void setFeeBrokerage(String feeBrokerage) {
		this.feeBrokerage = feeBrokerage;
	}
	
}
